char	*ft_strupcase(char *str)
{
	int	t;

	t = 0;
	while (str[t] != '\0')
	{
		if (str[t] > 96 && str[t] < 123)
			str[t] = (str[t] - 32);
		t++;
	}
	return (str);
}
