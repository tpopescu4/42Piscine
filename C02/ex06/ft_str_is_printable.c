int	ft_str_is_printable(char *str)
{
	int	t;
	int	r;

	t = 0;
	r = 1;
	if (str[t] == '\0')
		return (r);
	while (str[t] != '\0')
	{
		if (str[t] < 32 || str[t] == 127)
			r = 0;
		t++;
	}
	return (r);
}
