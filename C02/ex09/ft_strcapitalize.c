char	*ft_strcapitalize(char *str)
{
	int	t;

	t = 1;
	if (str[0] > 96 && str[0] < 123)
		str[0] = (str[0] - 32);
	while (str[t] != '\0')
	{
		if ((str[t - 1] > 96 && str[t - 1] < 123)
			|| (str[t - 1] > 64 && str[t] < 91)
			|| (str[t - 1] > 47 && str[t - 1] < 58))
		{
			if (str[t] > 64 && str[t] < 91)
				str[t] = (str[t] + 32);
		}
		if ((str[t - 1] < 97 && str[t - 1] > 90)
			|| (str[t - 1] < 65 && str[t - 1] > 57)
			|| str[t - 1] < 48 || str[t - 1] > 122)
		{
			if (str[t] > 96 && str[t] < 123)
				str[t] = (str[t] - 32);
		}
		t++;
	}
	return (str);
}
