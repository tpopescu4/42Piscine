char	*ft_strlowcase(char *str)
{
	int	t;

	t = 0;
	while (str[t] != '\0')
	{
		if (str[t] > 64 && str[t] < 91)
			str[t] = (str[t] + 32);
		t++;
	}
	return (str);
}
