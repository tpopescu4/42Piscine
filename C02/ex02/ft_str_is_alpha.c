int	ft_str_is_alpha(char *str)
{
	int	t;
	int	r;

	t = 0;
	r = 1;
	if (str[t] == '\0')
		return (r);
	while (str[t] != '\0')
	{
		if ((str[t] < 65 || str[t] > 90) && (str[t] < 97 || str[t] > 122))
			r = 0;
		t++;
	}
	return (r);
}
