void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	b;
	int	swap;

	a = 0;
	b = size - 1;
	while (a < (size / 2 ))
	{
		swap = tab[a];
		tab[a] = tab[b];
		tab[b] = swap;
		a++;
		b--;
	}
}
